#!/usr/bin/env python3
"""
Analyze OMNIA-INVARIANCE Outputs · OMNIA-CONSTANT

Purpose:
    Analyze real outputs generated by OMNIA-INVARIANCE.

This script does NOT define new Ω metrics.

It only studies whether a persistent transition region
appears across existing OMNIA / OMNIA-INVARIANCE outputs.
"""

import json
import statistics
from pathlib import Path
from collections import defaultdict


# -------------------------------------------------------------------
# CONFIG
# -------------------------------------------------------------------

INPUT_DIR = Path("imported_results/invariance")

OUTPUT_DIR = Path("results/import_from_invariance")

SUMMARY_FILE = OUTPUT_DIR / "invariance_analysis_summary.json"
ASCII_REPORT = OUTPUT_DIR / "invariance_analysis_ascii.txt"

TRANSITION_LOW = 0.55
TRANSITION_HIGH = 0.65


# -------------------------------------------------------------------
# HELPERS
# -------------------------------------------------------------------

def load_jsonl(path):
    rows = []

    if not path.exists():
        return rows

    with path.open("r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()

            if not line:
                continue

            try:
                row = json.loads(line)
            except json.JSONDecodeError:
                continue

            row["_source_file"] = path.name
            rows.append(row)

    return rows


def load_all_results(input_dir):
    rows = []

    if not input_dir.exists():
        return rows

    for path in sorted(input_dir.glob("*.jsonl")):
        rows.extend(load_jsonl(path))

    return rows


def extract_omega(row):
    """
    Canonical Ω extraction priority.
    """

    for key in [
        "omega_score",
        "omega",
        "omega_mean",
    ]:
        value = row.get(key)

        if isinstance(value, (int, float)):
            return float(value)

    return None


def classify_domain(row):
    text = " ".join([
        str(row.get("domain", "")),
        str(row.get("experiment_name", "")),
        str(row.get("_source_file", "")),
    ]).lower()

    if "noise" in text or "random" in text:
        return "random"

    if "symbolic" in text:
        return "symbolic"

    if "graph" in text:
        return "graph"

    if "financial" in text:
        return "financial"

    if "crypto" in text:
        return "crypto"

    if "topology" in text:
        return "topology"

    if "chaotic" in text:
        return "chaotic"

    if "oscill" in text:
        return "oscillatory"

    if "persistent" in text:
        return "persistent"

    if "normalized" in text:
        return "normalized"

    if "raw" in text:
        return "raw"

    return "unknown"


def summarize(values):
    if not values:
        return {
            "count": 0,
        }

    transition_values = [
        v for v in values
        if TRANSITION_LOW <= v <= TRANSITION_HIGH
    ]

    return {
        "count": len(values),
        "mean": statistics.mean(values),
        "std": statistics.pstdev(values),
        "min": min(values),
        "max": max(values),
        "transition_count": len(transition_values),
        "transition_rate": len(transition_values) / len(values),
    }


def ascii_bar(rate, width=40):
    filled = int(rate * width)
    return "#" * filled + "-" * (width - filled)


# -------------------------------------------------------------------
# MAIN
# -------------------------------------------------------------------

def main():
    rows = load_all_results(INPUT_DIR)

    grouped = defaultdict(list)

    all_omegas = []

    for row in rows:
        omega = extract_omega(row)

        if omega is None:
            continue

        domain = classify_domain(row)

        grouped[domain].append(omega)

        all_omegas.append(omega)

    summary = {
        "transition_region": [
            TRANSITION_LOW,
            TRANSITION_HIGH,
        ],
        "total_rows_loaded": len(rows),
        "total_omega_values": len(all_omegas),
        "overall": summarize(all_omegas),
        "domains": {
            domain: summarize(values)
            for domain, values in sorted(grouped.items())
        },
    }

    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    with SUMMARY_FILE.open("w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    lines = []

    lines.append("=" * 80)
    lines.append("OMNIA-INVARIANCE OUTPUT ANALYSIS")
    lines.append("=" * 80)
    lines.append("")

    lines.append("OVERALL")
    lines.append("-" * 80)

    overall = summary["overall"]

    lines.append(json.dumps(overall, indent=2))
    lines.append("")

    if overall.get("transition_rate") is not None:
        lines.append(
            "transition density: "
            + ascii_bar(overall["transition_rate"])
        )

    lines.append("")
    lines.append("")

    for domain, stats in sorted(summary["domains"].items()):
        lines.append("=" * 80)
        lines.append(f"DOMAIN: {domain}")
        lines.append("=" * 80)

        lines.append(json.dumps(stats, indent=2))
        lines.append("")

        if stats.get("transition_rate") is not None:
            lines.append(
                "transition density: "
                + ascii_bar(stats["transition_rate"])
            )

        lines.append("")
        lines.append("")

    with ASCII_REPORT.open("w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    print("=" * 80)
    print("OMNIA-INVARIANCE OUTPUT ANALYSIS")
    print("=" * 80)
    print(json.dumps(summary, indent=2))
    print()
    print(f"Summary written to: {SUMMARY_FILE}")
    print(f"ASCII report written to: {ASCII_REPORT}")


if __name__ == "__main__":
    main()